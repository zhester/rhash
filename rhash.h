#ifndef _RHASH_H
#define _RHASH_H

/*--------------------------------------------------------------------
                           GENERAL INCLUDES
--------------------------------------------------------------------*/

#include <stdint.h>

/*--------------------------------------------------------------------
                          LITERAL CONSTANTS
--------------------------------------------------------------------*/

/*--------------------------------------------------------------------
                                TYPES
--------------------------------------------------------------------*/

typedef int rhash_result_t;         /* result type                  */

enum {                              /* results                      */
    RHASH_PARAM  = -99,             /* invalid parameter            */
    RHASH_MEMORY,                   /* user memory too short        */
    RHASH_STRING,                   /* invalid string argument      */
    RHASH_RES_OK = 0                /* operation successful         */
};

/*--------------------------------------------------------------------
                           MEMORY CONSTANTS
--------------------------------------------------------------------*/

/*--------------------------------------------------------------------
                              VARIABLES
--------------------------------------------------------------------*/

/*--------------------------------------------------------------------
                                MACROS
--------------------------------------------------------------------*/

/*--------------------------------------------------------------------
                              PROCEDURES
--------------------------------------------------------------------*/

/*----------------------------------------------------------
rhash.c functions
----------------------------------------------------------*/

rhash_result_t rhash_decode(        /* decode a hash                */
    uint64_t*           id,         /* user's identifier memory     */
    const char*         hash        /* user's hash to decode        */
);                                  /* decode result                */

rhash_result_t rhash_encode(        /* encode an identifier         */
    char*               hash,       /* user's memory to store hash  */
    size_t              size,       /* size (bytes) of user's memory*/
    uint64_t            id          /* 64-bit resource identifier   */
);                                  /* encode result                */

#endif /* _RHASH_H */
